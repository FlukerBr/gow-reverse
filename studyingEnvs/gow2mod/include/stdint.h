#define _WORD  uint16_t
#define _DWORD uint32_t
#define _QWORD uint64_t
// #define _LONGLONG __int128

#define BYTEn(x, n)   (*((_BYTE*)&(x)+n))
#define WORDn(x, n)   (*((_WORD*)&(x)+n))
